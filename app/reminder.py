def build_nudge(top_pattern: PatternScore | None) -> str:
    if top_pattern is None:
        return (
            "Пока данных для персональной подсказки мало. Сохрани ещё несколько инсайтов, "
            "и я смогу показать устойчивые темы."
        )

    templates = REMINDER_TEMPLATES.get(top_pattern.key)
    if not templates:
        return (
            f"Сейчас главная тема: {top_pattern.key}. "
            "Сделай маленький шаг на 10 минут в сторону этой темы."
        )

    idx = 0 if top_pattern.score >= 0.5 else 1
    return templates[idx]
